def add(x,y):
    sum=x+y
    print(f"Sum of {x} and {y} is:{sum}")
def sub(x,y):
    sub=x-y
    print(f"Substraction of {x} and {y} is:{sub}")
def mul(x,y):
    mul=x*y
    print(f"Multiplication of {x} and {y} is:{mul}")
def div(x,y):
    div=x//y
    print(f"Division of {x} and {y} is:{div}")


